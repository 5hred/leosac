#include <cassert>
#include <tools/log.hpp>
#include "Task.h"

using namespace Leosac;

bool Task::is_complete() const
{
    assert(my_future.valid());
    return my_future.wait_for(std::chrono::milliseconds(0)) == std::future_status::ready;
}
